package com.derbysoft.entity.sys;

import dy.hrtworkframe.annotation.Key;
import dy.hrtworkframe.annotation.Table;

/**
 * 数据权限
 * 
 * @author jiangtao 18674842761
 */
@Table(name="SYS_RoleAccess")
public class SYS_RoleAccess {
	 
	/**
	 * 主键
	 */
	@Key
	private String roacID;

	/**
	 * 用户ID
	 */
	private String roleID;

	/**
	 * 用户名称
	 */
	private String roleName;

	/**
	 * 数据类型
	 */
	private String dataType;

	/**
	 * 数据值
	 */
	private String dataValue;

	/**
	 * 录入时间
	 */
	private String inputDate;

	/**
	 * 录入人员
	 */
	private String inputName;

	public String getRoacID() {
		return roacID;
	}

	public void setRoacID(String roacID) {
		this.roacID = roacID;
	}

	public String getRoleID() {
		return roleID;
	}

	public void setRoleID(String roleID) {
		this.roleID = roleID;
	}

	public String getRoleName() {
		return roleName;
	}

	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}

	public String getDataType() {
		return dataType;
	}

	public void setDataType(String dataType) {
		this.dataType = dataType;
	}

	public String getDataValue() {
		return dataValue;
	}

	public void setDataValue(String dataValue) {
		this.dataValue = dataValue;
	}

	public String getInputDate() {
		return inputDate;
	}

	public void setInputDate(String inputDate) {
		this.inputDate = inputDate;
	}

	public String getInputName() {
		return inputName;
	}

	public void setInputName(String inputName) {
		this.inputName = inputName;
	}

	
}
