package com.derbysoft.entity.echarts;

public class EchartsDemand {

	
	private String year;
	
	private String day;
	
	private String mon;
    
	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getDay() {
		return day;
	}

	public void setDay(String day) {
		this.day = day;
	}

	public String getMon() {
		return mon;
	}

	public void setMon(String mon) {
		this.mon = mon;
	}
	
	
}
