package com.derbysoft.entity.cms;

import dy.hrtworkframe.annotation.Key;
import dy.hrtworkframe.annotation.Table;
import dy.hrtworkframe.annotation.Temporary;

@Table(name="cms_news")
public class News {
   @Key  
	private String newsID;
    
    
    private String updateTime;
   
	private String userID;
	
	private String summary;
	
	private String title;
	
	private String context;
	
	private String createTime;
	//判断他的状态1到6分别为警员新闻,群众新闻,通用新闻,警员通告,群众通告,通用通告
	private String state;
	//看是否收藏了1为收藏  0为未收藏
	@Temporary
	private String collectState;
	//写入人的名字
	private String realName;
	//推送状态
	private String pushStatus; 
	//焦点状态
	private String focusStatus;
    //发布时间
	private String  publishTime;
	//发布状态
	private String  publishState;
	//推送次数
	private String  pushNum;
	
    private String infoType;
    //图片上传的地址
    private String filePath;
    //保存的绝对地址
    private String savepath;
  
    
    

	public String getSavepath() {
		return savepath;
	}
	public void setSavepath(String savepath) {
		this.savepath = savepath;
	}
	public String getFilePath() {
		return filePath;
	}
	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}

    private String url;
    
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getInfoType() {
		return infoType;
	}
	public void setInfoType(String infoType) {
		this.infoType = infoType;
	}
	public String getPushNum() {
		return pushNum;
	}
	public void setPushNum(String pushNum) {
		this.pushNum = pushNum;
	}
	public String getPublishState() {
		return publishState;
	}
	public void setPublishState(String publishState) {
		this.publishState = publishState;
	}
	public String getPublishTime() {
		return publishTime;
	}
	public void setPublishTime(String publishTime) {
		this.publishTime = publishTime;
	}
	public String getFocusStatus() {
		return focusStatus;
	}
	public void setFocusStatus(String focusStatus) {
		this.focusStatus = focusStatus;
	}
	public String getRealName() {
		return realName;
	}
	public void setRealName(String realName) {
		this.realName = realName;
	}
	public String getNewsID() {
		return newsID;
	}
	public void setNewsID(String newsID) {
		this.newsID = newsID;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getSummary() {
		return summary;
	}
	public void setSummary(String summary) {
		this.summary = summary;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContext() {
		return context;
	}
	public void setContext(String context) {
		this.context = context;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}
	public String getCollectState() {
		return collectState;
	}
	public void setCollectState(String collectState) {
		this.collectState = collectState;
	}
	public String getPushStatus() {
		return pushStatus;
	}
	public void setPushStatus(String pushStatus) {
		this.pushStatus = pushStatus;
	}    
	
}
